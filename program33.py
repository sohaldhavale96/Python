# Module
# A module is a file containing python code. This code can have functions, classes and variables. Modules helps organize related code into files that can be easily reused and imported into other Python scripts.

# Python's standard library is a collection of modules that provide ready-to-use functionality for tasks like I/O, networking, and more.

# Complete module
import module_name

# Import as alias
import module_name as alias_name

# Specific Item
from module_name import item_name

# Import specific item from a module and provide alias to that
from module_name import item_name as alias_name

